import { defineStore } from "pinia"
import { ref } from "vue"

export const useNotificationStore = defineStore('notificationStore', () => {

    /* -------------------------------------------------------------------------- */
    /*                                   STATES                                   */
    /* -------------------------------------------------------------------------- */
    const isOpen = ref(false)


    /* -------------------------------------------------------------------------- */
    /*                                   METHODS                                  */
    /* -------------------------------------------------------------------------- */
    const toggle = () => {
        isOpen.value = !isOpen.value
    }



    /* -------------------------------------------------------------------------- */
    /*                                   RETURN                                   */
    /* -------------------------------------------------------------------------- */
    return {
        isOpen,
        toggle
    }

})